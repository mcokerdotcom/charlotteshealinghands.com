<h1>Community Building Grant</h1>

<p>
This initiative was born as a way to build community within my healing practice. Gratuity in my practice have never been expected, but on many occasions people feel inclined to do so.   Now all gratuity will go into a community grant fund that will supplement sessions for The Arvigo Techniques of <a href="/services#Abdominal">Mayan Abdominal Therapy</a>.
</p>

<p>
It is a pay it forward community building opportunity.  When you feel compelled to give more, someone else in the community benefits from your generosity! 
</p>
 
<p>
The goal here is to empower clients in their own healing process and the Arvigo Techniques of Mayan Abdominal Therapy can help to clear trauma from the body while working towards balancing the organ systems.  I also teach a self-care massage during this session, which when done regularly is the gift that keeps on giving.
</p>
 
<h2>How much are the grants?</h2>

<p>
The grant can be up to 50% off of the treatment.  When you submit your request, please let me know how much you can budget for your session.  For example an initial session is 2hrs and is normally $200.  You can ask to pay any where between $100- $200 depending on your budget.  If you haven't been able to afford to come in for a follow-up session that you feel like you really need, you can pay $75- $150 ($150 being the normal price for a 1.5hr follow-up).
</p>
 
<h2>Who is eligible for these grants?</h2>

<p>
If your heart is calling you to this work and you don't have the funds to afford it, you are "eligible".
</p>

<p>
Limit one grant per person so that more people can benefit from this opportunity.  Exceptions may be made, if there are an abundance of funds.
</p>
 
<h2>How to apply?</h2>

<p>
To apply for a grant simply submit a minimum of three sentences and maximum of a half of a page describing why you are applying for a grant for the session and the amount of assistance you are requesting (up to 50% off of your session).  This is requirement is a necessary part of the empowerment process.  I know that asking for help can be challenging, but I believe it to be an important part of the process.
</p>
