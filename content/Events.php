<p>
This 3-day class prepares the student to perform The Arvigo Techniques of Maya Abdominal Therapy™ on him/herself only. Additionally, this class is a prerequisite to enrollment in the Professional Training for licensed healthcare practitioners.
</p>

<p>
The Arvigo techniques of Maya Abdominal Therapy is a non-invasive, external, massage technique. It guides internal abdominal organs into their proper position for optimum health and well-being. Arvigo Therapy improves organ function by releasing physical and emotional congestion from the abdomen. This is effective for both men and women.
</p>

<p>
The technique applies anatomy, physiology, herbology and naprapathy, (study of the ligaments, joints and muscles), with Ancient Maya healing techniques to address common female and male complaints. The technique works by relieving congestion and blockages to improve the flow of chi and fluids of the circulatory, lymphatic and nervous systems to prevent the progression of chronic disease symptomology.
</p>

<p>
Self-Care Training covers anatomy and physiology of the abdominal and reproductive organs, addresses causes and symptoms of mal-positioned organs and demonstrates self-care techniques. Self-Care also addresses herbal, nutritional, spiritual and emotional support for the massage techniques to ensure comprehensive understanding of the modality and how it contributes to wellness.
</p>

<p>
This Self-Care Training workshop is conducted by: Kirsten Kern, PhD, LMTI and certified ATMAM Self-Care™ instructor and Charlotte Westbrook, LMTI and ATMAM Self-Care™ instructor.
</p>

<p>Prerequisites: Anyone may register for this class. No prior training or licensing is required.</p>

