<h1>Cancellation/Late Policy:</h1>

<p>In my desire to be effective and fair to all of my clients and out of consideration for my time, I have adopted the following policies: </p>

<ul>
<li><b>24 hour advance notice is required</b> when canceling an appointment. This allows the opportunity for someone else to schedule an appointment.</li>
<li>If you are unable to give 24 hours advance notice you will be charged the <b>full amount</b> of your appointment. This amount must be paid prior to your next scheduled appointment.</li>
</ul>

<h2>No-shows</h2>
<p>Anyone who either forgets or consciously chooses to forgo his or her appointment for whatever reason will be considered a "no-show." They will be charged for their "missed" appointment and future service will be denied until payment is made.</p>

<h2>Arriving late</h2>
<p>Appointment times have been arranged specifically for you. If you arrive late your session may be shortened in order to accommodate others whose appointments follow yours. Depending upon how late you arrive, your therapist will then determine if there is enough time remaining to start a treatment. Regardless of the length of the treatment actually given, <b>you will be responsible for the "full" session</b>.</p>

<p>Out of respect and consideration to your therapist and other customers, please plan accordingly and be on time.</p>
